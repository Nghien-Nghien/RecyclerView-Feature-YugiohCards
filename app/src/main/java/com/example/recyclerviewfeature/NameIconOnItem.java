package com.example.recyclerviewfeature;

public class NameIconOnItem {

    public String dataName;
    public int dataIcon;

    public NameIconOnItem(String dataName, int dataIcon) {
        this.dataName = dataName;
        this.dataIcon = dataIcon;
    }

    public String getDataName() {
        return dataName;
    }

    public int getDataIcon() {
        return dataIcon;
    }

}
